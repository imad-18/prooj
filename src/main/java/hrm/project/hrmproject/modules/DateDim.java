package hrm.project.hrmproject.modules;

public class DateDim {
    private int idStartDate;
    private int day;
    private int month;
    private int year;

    // Getters and Setters
    public int getIdStartDate() {
        return idStartDate;
    }

    public void setIdStartDate(int idStartDate) {
        this.idStartDate = idStartDate;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }
}
